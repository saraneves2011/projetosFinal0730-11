    private void jTablePedidosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTablePedidosMouseClicked
        // TODO add your handling code here:
        jTextAreaCozinha.setText("");

        int linha = jTablePedidos.getSelectedRow();
        int coluna = jTablePedidos.getSelectedColumn();
        pedido = (int) jTablePedidos.getValueAt(linha, coluna);
        ArrayList<RelPedidoProduto> listaPedido = new RelPedidoProdutoDAO().consultarItem(pedido);

        for (RelPedidoProduto ped : listaPedido) {
            jTextAreaCozinha.append(String.valueOf(ped.getProduto()) + " "
                    + String.valueOf(ped.getQuantidadeProd()) + "   " + "\n");
        }


    }//GEN-LAST:event_jTablePedidosMouseClicked

