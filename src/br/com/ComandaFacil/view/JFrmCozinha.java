    private void listaCozinha() {
        ArrayList<RelPedidoProduto> pedidos = new RelPedidoProdutoDAO().consultarProdutos();

        if (pedidos.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhum pedido encontrado");
        } else if (!relPedidoProduto.isLiberado()) {
            jTablePedidos.getColumnModel().getColumn(0);
            jTablePedidos.getColumnModel().getColumn(1);

            DefaultTableModel modelo = (DefaultTableModel) jTablePedidos.getModel();

            for (RelPedidoProduto rpp : pedidos) {
                jLabelMesa.setText("");
                modelo.addRow(new Object[]{rpp.getId(), rpp.getIdPedido()});
                jLabelMesa.setText(String.valueOf(rpp.getId()));
            }

        }
    }

