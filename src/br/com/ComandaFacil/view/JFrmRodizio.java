    private void jButtonFecharPedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonFecharPedidoActionPerformed
        // TODO add your handling code here:

        try {
            //adicionando idCliente dentro do objeto pedido para que seja salvo corretamente no BD
            pedido.setIdCliente(cliente.getIdCliente());

            //salva o pedido no BD e rotorna para variavel resPedido o pedido com informação do ID
            Pedido resPedido = pedidoDao.fazerPedido(pedido);

            if (resPedido.getIdPedido() != null) {
                pedido.getRelPedidoProduto().forEach((rpp) -> {
                    rpp.setIdPedido(resPedido.getIdPedido());
                    relPedidoProdutoDao.incluirProduto(rpp);
                });
            }

            JOptionPane.showMessageDialog(null, "OPERAÇÃO FECHADA COM SUCESSO.");
            JFrmMenu frmmenu = new JFrmMenu();
            frmmenu.setVisible(true);
            dispose();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro inesperado ao Incluir: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonFecharPedidoActionPerformed

