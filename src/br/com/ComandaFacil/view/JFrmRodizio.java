    public void inserirMesa() {
        String numMesa = JOptionPane.showInputDialog(this, "Digite o número da mesa");

        if (numMesa == null) {
            JFrmMenu frmmenu = new JFrmMenu();
            frmmenu.setVisible(true);
            // this.setVisible(false);
            dispose();
        } else {
            while (numMesa.equals("")) {

                if (numMesa.equals("")) {
                    numMesa = JOptionPane.showInputDialog(this, "Digite o número da mesa");
                }
            }

            pedido.setNumMesa(numMesa);
            pedido.setIdCliente((Sessao.idCliente));

            pedido.setDataPedido(new Date());
            jLabelMesa.setText(pedido.getNumMesa());
            jLabel2NumeroPedido.setText(String.valueOf(pedidoDao.autPedido().getIdPedido()));
        }

    }

