    private void jButtonAdicionarItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarItemActionPerformed
        // TODO add your handling code here:

        if (!jComboBoxItemProdutos.getSelectedItem().toString().equals("")) {

            jListProdutos.setModel(model);
        }

        if (Integer.parseInt(txtQuant.getText()) <= 10) {

            RelPedidoProduto relacionamento = new RelPedidoProduto();

            relacionamento.setIdPedido(pedidoDao.autPedido().getIdPedido());
            relacionamento.setIdProduto(id_prod.get(jComboBoxItemProdutos.getSelectedIndex()));
            relacionamento.setQuantidadeProd(Integer.parseInt(txtQuant.getText()));

            // banco de dados
            //relPedidoProdutoDao.incluirProduto(relacionamento);
            pedido.addRelPedidoProduto(relacionamento);
            model.addElement(jComboBoxItemProdutos.getSelectedItem().toString() + " " + txtQuant.getText());
        } else {
            JOptionPane.showMessageDialog(null, "A quantidade máxima deve ser de 10 unidades!");
        }

        // ArrayList<ItemProduto> produtos = pedido.getProdutos();
        // itemProduto.setIdProduto(jComboBox1.get);    }
    }//GEN-LAST:event_jButtonAdicionarItemActionPerformed

    public void testeConex() {
        try {

            ctrlProduto ctrlProduto = new ctrlProduto();
            jComboBoxItemProdutos.removeAllItems();

            ResultSet rs = ctrlProduto.consultarPedidosCombo();

            while (rs.next()) {
                relPedidoProduto.setIdProduto(rs.getInt("id_prod"));
                id_prod.addElement(rs.getInt("id_prod"));
                jComboBoxItemProdutos.addItem(rs.getString(2));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Carregar produto" + e);

        }
    }

